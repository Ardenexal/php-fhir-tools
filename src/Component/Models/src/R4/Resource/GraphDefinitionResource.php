/**
 * @author Health Level Seven International (FHIR Infrastructure)
 *
 * @see http://hl7.org/fhir/StructureDefinition/GraphDefinition
 *
 * @description A formal computable definition of a graph of resources - that is, a coherent set of resources that form a graph by following references. The Graph Definition resource defines a set and makes rules about the set.
 */
#[FhirResource(
    type: 'GraphDefinition',
    version: '4.0.1',
    url: 'http://hl7.org/fhir/StructureDefinition/GraphDefinition',
    fhirVersion: 'R4',
)]
class GraphDefinitionResource extends DomainResourceResource
{
    public function __construct(
        /** @var string|null id Logical id of this artifact */
        public ?string $id = null,
        /** @var Meta|null meta Metadata about the resource */
        public ?Meta $meta = null,
        /** @var UriPrimitive|null implicitRules A set of rules under which this content was created */
        public ?UriPrimitive $implicitRules = null,
        /** @var string|null language Language of the resource content */
        public ?string $language = null,
        /** @var Narrative|null text Text summary of the resource, for human interpretation */
        public ?Narrative $text = null,
        /** @var array<ResourceResource> contained Contained, inline Resources */
        public array $contained = [],
        /** @var array<Extension> extension Additional content defined by implementations */
        public array $extension = [],
        /** @var array<Extension> modifierExtension Extensions that cannot be ignored */
        public array $modifierExtension = [],
        /** @var UriPrimitive|null url Canonical identifier for this graph definition, represented as a URI (globally unique) */
        public ?UriPrimitive $url = null,
        /** @var StringPrimitive|string|null version Business version of the graph definition */
        public StringPrimitive|string|null $version = null,
        /** @var StringPrimitive|string|null name Name for this graph definition (computer friendly) */
        #[NotBlank]
        public StringPrimitive|string|null $name = null,
        /** @var PublicationStatusType|null status draft | active | retired | unknown */
        #[NotBlank]
        public ?PublicationStatusType $status = null,
        /** @var bool|null experimental For testing purposes, not real usage */
        public ?bool $experimental = null,
        /** @var DateTimePrimitive|null date Date last changed */
        public ?DateTimePrimitive $date = null,
        /** @var StringPrimitive|string|null publisher Name of the publisher (organization or individual) */
        public StringPrimitive|string|null $publisher = null,
        /** @var array<ContactDetail> contact Contact details for the publisher */
        public array $contact = [],
        /** @var MarkdownPrimitive|null description Natural language description of the graph definition */
        public ?MarkdownPrimitive $description = null,
        /** @var array<UsageContext> useContext The context that the content is intended to support */
        public array $useContext = [],
        /** @var array<CodeableConcept> jurisdiction Intended jurisdiction for graph definition (if applicable) */
        public array $jurisdiction = [],
        /** @var MarkdownPrimitive|null purpose Why this graph definition is defined */
        public ?MarkdownPrimitive $purpose = null,
        /** @var ResourceTypeType|null start Type of resource at which the graph starts */
        #[NotBlank]
        public ?ResourceTypeType $start = null,
        /** @var CanonicalPrimitive|null profile Profile on base resource */
        public ?CanonicalPrimitive $profile = null,
        /** @var array<GraphDefinitionLink> link Links this graph makes rules about */
        public array $link = [],
    ) {
        parent::__construct($id, $meta, $implicitRules, $language, $text, $contained, $extension, $modifierExtension);
    }
}
